// Hanzi of every word allocated to stages 1..index — the vocabulary a stage's
// exercises are allowed to draw on.
export function allowedHanziThrough(
  index: number,
  hanziOf: (id: string) => string | undefined,
): string[] {
  const out: string[] = [];
  for (let i = 1; i <= index; i++)
    for (const id of STAGE_WORD_IDS[i] ?? []) {
      const h = hanziOf(id);
      if (h) out.push(h);
    }
  return out;
}
